#include<iostream>
using namespace std;
int main()
{    long long int n,k;
        cin>>n>>k;
        long int p[k]={0};
    for(int i=1;i<=k;i++)
    {
        if(n%(i%k)==0){
            p[i]=i;
        }
    }
    for(int i=1;i<=k;i++)
    {
        if(i==p[i]*((n-p[i])/k));
        {
            cout<<i;
            break;
        }
    }
}
